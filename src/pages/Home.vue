<script>
import { Line } from 'vue-chartjs';
import dayjs from 'dayjs';
import {
  Chart as ChartJS,
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement,
  Title,
  Tooltip,
  Legend,
} from 'chart.js';

ChartJS.register(
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement,
  Title,
  Tooltip,
  Legend
);

export default {
  components: { Line },
  data() {
    return {
      data: {
        ranking: {},
        progress: [],
      },
      loaded: false,
      chapterCharts: [],
      chartOptions: {
        responsive: true,
        maintainAspectRatio: false,
      },
    };
  },
  inject: ['http'],
  async mounted() {
    this.loaded = false;
    console.log(this.$route.params);
    const response = await this.http.get('/users/dashboard');
    this.data = {
      progress: Object.values(response.data.data.progress.grouped),
      ranking: response.data.data.ranking,
    };

    const getAnswers = (chapter, mode) => {
      return chapter.sections.map((s) => {
        console.log('challenges', s);
        const challenge = s.challenges.find((c) => c.difficulty === mode);
        let challengeAnswer = null;
        if (challenge && s.answers) {
          challengeAnswer = s.answers.find(
            (a) => a.challenge_id === challenge.id
          );
          challenge.answer = challengeAnswer;
        }
        if (challengeAnswer) {
          return challengeAnswer.time_taken;
        }
        return 0;
      });
    };

    const chapterDatasets = {};

    for (const chapter of this.data.progress) {
      const labels = chapter.sections.map(
        (s) => `C${chapter.order}S${s.order}`
      );
      const easyDataset = getAnswers(chapter, 'easy');
      const mediumDataset = getAnswers(chapter, 'medium');
      const hardDataset = getAnswers(chapter, 'hard');

      chapterDatasets[chapter.id] = {
        chapter: { ...chapter },
        chartData: {
          labels: labels,
          datasets: [
            {
              label: 'Easy',
              backgroundColor: 'green',
              borderColor: 'green',
              pointStyle: 'rectRounded',
              pointRadius: 5,
              pointBorderColor: 'white',
              borderWidth: 1,
              data: easyDataset,
            },
            {
              label: 'Medium',
              backgroundColor: 'yellow',
              borderColor: 'yellow',
              pointStyle: 'circle',
              pointRadius: 5,
              pointBorderColor: 'white',
              borderWidth: 1,
              data: mediumDataset,
            },
            {
              label: 'Hard',
              backgroundColor: 'red',
              borderColor: 'red',
              pointStyle: 'rectRot',
              pointRadius: 5,
              pointBorderColor: 'white',
              borderWidth: 1,
              data: hardDataset,
            },
          ],
        },
      };
      console.log(easyDataset, mediumDataset, hardDataset);
    }

    this.chapterCharts = Object.values(chapterDatasets);

    this.loaded = true;
  },
  methods: {
    toggleChapter(chapter) {
      if (typeof chapter.expanded === 'undefined') {
        chapter.expanded = false;
      }
      chapter.expanded = !chapter.expanded;
      console.log(chapter);
    },
    toggleSection(section) {
      if (typeof section.expanded === 'undefined') {
        section.expanded = false;
      }
      section.expanded = !section.expanded;
      console.log(section);
    },
    formatDate(date) {
      const dt = dayjs(date);
      return dt.format('YYYY-MM-DD HH:mm:ss');
    },
    formatTime(seconds) {
      let hours = Math.floor(seconds / 3600);
      let minutes = Math.floor((seconds - hours * 3600) / 60);
      seconds = seconds - hours * 3600 - minutes * 60;

      if (hours < 10) {
        hours = '0' + hours;
      }
      if (minutes < 10) {
        minutes = '0' + minutes;
      }
      if (seconds < 10) {
        seconds = '0' + seconds;
      }
      return hours + 'h:' + minutes + 'm:' + seconds + 's';
    },
  },
};
</script>
